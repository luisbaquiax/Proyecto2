public class CasillaInicio {

    private int dineroAdar;
    private String nombre;

    public CasillaInicio() {

        this.nombre = "INICIO";
    }

    public void setDineroAdar(int dineroAdar) {
        this.dineroAdar = dineroAdar;
    }

    public int getDineroAar() {
        return dineroAdar;
    }

    public String getNombre() {
        return nombre;
    }

}
